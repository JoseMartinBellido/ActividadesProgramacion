package prog.unidad04.atributos.ejercicio03;

public class Lavadora {

  // Constantes del color
  
  /**
   * String. Constante. Color blanco.
   */
  public static final String COLOR_BLANCO = "blanco";
  
  /**
   * String. Constante. Color negro.
   */
  public static final String COLOR_NEGRO = "negro";
  
  /**
   * String. Constante. Color rojo.
   */
  public static final String COLOR_ROJO = "rojo";
  
  /**
   * String. Constante. Color azul.
   */
  public static final String COLOR_AZUL = "azul";
  
  /**
   * String. Constante. Color gris.
   */
  public static final String COLOR_GRIS = "gris";
  
  /**
   * double. Precio base de la lavadora indicada en €.
   */
  public double precioBase;
  
  /**
   * String. Color de la lavadora. Debe ser blanco, negro, rojo, azul o gris.
   */
  public String color;
  
  /**
   * char. Consumo energético. Debe ser una letra entre la A y la F mayúscula.
   */
  public char consumoEnergetico;
  
  /**
   * double. Peso de la lavadora en kg. Debe ser mayor que 0.
   */
  public double peso;
  
  /**
   * double. Carga máxima de la lavadora en kg. Debe ser mayor que 0.
   */
  public double cargaMaxima;
  
  
  
}
