package prog.unidad04.atributos.ejercicio02;

public class Persona {

  // Constantes de sexo de la persona
  
  /**
   * char. Constante. Letra que indica el sexo: hombre
   */
  public static final char SEXO_HOMBRE = 'H';
  
  /**
   * char. Constante. Letra que indica el sexo: mujer
   */
  public static final char SEXO_MUJER = 'M';
  
  
  /**
   * String. Nombre completo de la persona.
   */
  public String nombre;
  
  /**
   * int. Edad de la persona.
   */
  public int edad;
  
  /**
   * String. DNI de la persona.
   */
  public String dni;
  
  /**
   * String. Sexo de la persona. Debe ser "H" o "M".
   */
  public char sexo;
  
  /**
   * double. Peso de la persona en kg.
   */
  public double peso;
  
  /**
   * double. Altura de la persona en metros.
   */
  public double altura;
  
}
